<td><b><a name="Guilds" class="manlinks">Guilds</a></b></td>
<tr>
<td>
Guilds are for players who wish to group together and set goals to expand and raise their skills. Guilds are able to have bigger compounds, warehouses, and Reserves. If you would like a detail version of what the sizes are visit <a href="" class="manlinks">Guild Information</a>. 
</td>
</tr>
<tr>
<td>
-Creating Guilds; <br>
To create a guild you will have to fulfill the requirements;<br>
You must have 3 people sign the guild charter to create a guild. <br>
You must not name the guild anything that is deemed inappropriate by game administration. If you create an inappropriate guild name and tag, they will be renamed by administration without warning.
</td>
</tr>
<tr>
<td>
-Joining Guilds;<br>
To join a guild, you will need to either apply in the guild tab, or have a guild request sent to you by the guild admins. 
</td>
</tr>
<tr>
<td>
-Disbanding a Guild;<br>
If you would like to disband a guild, you must have 60% of admins agree to the disbanding, or you have less than 3 players in the guild. Once your guild member count drops under 3 it will automatically disband without admin permission.
</td>
</tr>
