<td><b><a name="Houses" class="manlinks">House / Gardens</a></b></td>
<tr>
<td>
Houses are used to store resources through the game if you do not wish to have them in your inventory. There is 3 types of "Houses" for players to build. The types; Villas, Chateaus, Ranches. The sizes of the houses are way smaller than Guild Compounds due to it being only accessible by a single player. The owner of houses can give permissions to other players to access their houses. They can also restrict what type of action they have with the house.
</td>
</tr>
<tr>
<td>
Gardens within the game are attached to player houses and are used for the skills "Gathering / Gardening". There are different sizes depending on what type of house you own. These are not determined at the moment. Players that have gardens can give permissions to others to access their gardens. 
</td>
