<td><b><a name="Comm" class="manlinks">Communication / Chat </a></b></td>
<tr>
<td>
Communication in Fallout Chronicle is interaction between players through public and private channels. Public Channels are there to help with growth and expansion between the relationship of players. It is a privilege to have communication throughout the game, if abused it will be taken away. 
</td>
</tr>
<tr>
<td>
The chat has many features for players to use. The features consists of Emoticons to express yourself, whispers; which allow you to have a private conversation with another player, and public help, trade, and guild channels. You are able to click <a href="/manual/commands/" class="manlinks">commands</a> in the channel selection to see what type of commands you are able to use. <a href="/manual/chathistory/" class="manlinks">Chat History</a> is also in the channel selection for players who wish to read up on the previous communications between players if they missed something. 
</td>
