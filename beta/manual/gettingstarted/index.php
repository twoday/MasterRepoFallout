<td><b><a name="Intro" class="manlinks">Introduction</a></b><td>
<tr>
<td>
<b>Fallout Chronicle</b> is a persistent timer text, browser based game. With 13 primary skills, you can take your chances at fighting against the Halloran beasts in combat, test your endurance while travelling throughout towns in speed, or sitting at the docks casting your line while fishing. 
<br>
<br>
Welcome to the year 2248, where seven explorers set out to traverse World 4-K63, Halloran. They were sent by The Kingdom to find a new land to colonize. With dwindling resources and few places for their people to live, they're in a rush to expand. Not much is known of their origin, the seven explorers' homeland, only that it is slowly being over-populated and a lack of land and resources are bringing it to it's end. <br>
<br>
A few years after the explorers settled on Halloran, The Kingdom sent out dropships to help them with the expedition and to assist with building up a new nation. <br>
With the fallout happening on the homeland the explorers are setting up guilds on Halloran and are looking for brave souls to do their quests, help fight their battles against creatures and travel through the towns to collect resources to become the strongest. <br>
