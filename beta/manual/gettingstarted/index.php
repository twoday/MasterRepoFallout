<td><b><a name="Nav" class="manlinks">Navigation</a></b></td>
<tr>
<td>
Navigating through Halloran is very simple. To travel throughout the continents you will need to click on the map which is placed at the lower left. As the map opens, you will be able to click on the the town you would like visit. You must be adjacent to the town you want to travel towards. <br><br>
<b>ex:</b> If you are sitting in Verne Peninsula, an adjacent town is either Domecelle or Frigiliana. There is a small visible line leading to adjacent towns on the map. While you are travelling you have the option to "Turn Around" which cancels your trip. Travelling to different continents is achievable by clicking Docks at the port. <br><br>
