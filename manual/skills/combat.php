Types of Combat
<table class="insk">
<td>
There are two types of combat in Fallout Chronicle; Hunting and Combat. The difference of the two are:
<br>
Hunting: Hunting or more commonly known by Away From Keyboard / Non-Active Combat. The player is able to look away from his screen or do other activities while leveling their combat. Though there is a certain level in which you have to achieve to not be constantly watching the fight.
<br>
<br>
C-Combat: C-Combat is the quite opposite of Hunting, in which you have to be constantly watching the fight between you and opponent. C-Combat is known as "Constant" Combat. Once you reach over the levels of a C-Combat Type opponent, it will add effects in  your battle to keep it "Constant". 
