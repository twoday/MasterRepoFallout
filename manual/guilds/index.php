<h2>Guilds</h2>
<a href="/manual/guilds/compounds" class="manlinks">Compound Information</a> | <a href="/manual/guilds/shops" class="manlinks"> Guild Shops </a>
<br>
<br>
Guilds in Fallout Chronicle are what are known as “Chartered Guilds”. This means you have to have at least 3 people sign the guild agreement (Charter) in order to even start a guild. Guilds bring players together of the same interests and goals to expand their gameplay as they level. Guilds are made in this manner so that we can help each other advance in our skills and "Chronicle". However, being chartered guilds also means that if at any time the guild drops to less than 3 members the guild automatically gets disbanded.
<br />
<br />
<h3>Guild Compounds</h3>
